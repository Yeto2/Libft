#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	int		slen;
	char	*res;

	i = 0;
	if (!s)
		return (0);
	slen = ft_strlen(s);
	res = (char *)malloc(sizeof(char) * slen + 1);
	if (!res)
		return (0);
	while (s[i])
	{
		res[i] = f(i, s[i]);
		i++;
	}
	res[i] = '\0';
	return (res);
}
